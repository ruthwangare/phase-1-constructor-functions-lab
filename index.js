// Scooter class
class Scooter {
    constructor(year, color, model) {
      this.year = year;
      this.color = color;
      this.model = model;
    }
  }

  // Driver class
  class Driver {
    constructor(name, age, experience) {
      this.name = name;
      this.age = age;
      this.experience = experience;
    }
  }

  // PickupLocation class
  class PickupLocation {
    constructor(address, city) {
      this.address = address;
      this.city = city;
    }
  }
